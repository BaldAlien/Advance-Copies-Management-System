<script  lang="ts">
import store from "../../store";
import { _login } from "../../api/auth/auth";
import { ElMessage } from "element-plus";
export default {
  name: "LoginForm",
  data() {
    let validatorName = (rule, value, callback) => {
      if (!value) {
        return callback(new Error("请输入用户名"));
      }
      callback();
    };
    let validatorPass = (rule, value, callback) => {
      if (value === "") {
        return callback(new Error("请输入密码"));
      }
      callback();
    };
    return {
      LoginFormData: {
        username: "",
        password: "",
      },
      LoginForm: [
        {
          title: "用户名",
          name: "username",
          value: "",
          meta: {
            max: 15,
          },
        },
        {
          title: "密码",
          name: "password",
          value: "",
          meta: {
            max: 30,
            type: "password",
          },
        },
      ],
      rules: {
        username: [
          {
            validator: validatorName,
            trigger: "blur",
          },
        ],
        password: [
          {
            validator: validatorPass,
            trigger: "blur",
          },
        ],
      },
      ready: false,
    };
  },
  methods: {
    login(LoginFormData) {
      this.$refs[LoginFormData].validate((valid) => {
        if (valid) {
          console.log(this.LoginFormData);
          _login(this.LoginFormData).then((res) => {
            if (res.data.token && res.code === 0) {
              localStorage.setItem("token", res.data.token);
              console.log(res.data.token);
              console.log("save token!");
              ElMessage.success({
                message: "登陆成功！",
                offset: 60,
                type: "success",
              });
              this.$router.push(this.$route.query.return);
            } else {
              ElMessage.error("登录失败！");
            }
          });
        } else {
          ElMessage.error("登录失败！");
        }
      });
    },
    changeEvent(newEvent) {
      if (newEvent === "alter") {
        return;
      }
      store.commit("setEvent", newEvent);
    },
  },
};
</script>

